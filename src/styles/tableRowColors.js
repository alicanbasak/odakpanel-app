export const showBgColor = value => {
  if (value === 1) {
    return "#cfd8dc";
  } else if (value === 2) {
    return "#c5cae9";
  } else if (value === 3) {
    return "#ffecb3";
  } else if (value === 4) {
    return "#c8e6c9";
  } else if (value === 5) {
    return "#ffcdd2";
  } else {
    return "#cfd8dc";
  }
};

export const showTextColor = value => {
  if (value === 1) {
    return "#607d8b";
  } else if (value === 2) {
    return "#3f51b5";
  } else if (value === 3) {
    return "#ffc107";
  } else if (value === 4) {
    return "#4caf50";
  } else if (value === 5) {
    return "#f44336";
  } else {
    return "#607d8b";
  }
};
